<script>
import { ref, reactive, watchEffect, toRefs } from 'vue'
import { useRoute } from 'vue-router'
import { get } from '@/utils/requests'
import { useCartEffect } from '../../effects/CartEffect'
// tabÂàóË°®
const tabList = [
  { id: 1, name: 'ÂÖ®ÈÉ®ÂïÜÂìÅ', tab: 'all' },
  { id: 2, name: 'ÁßíÊùÄ', tab: 'seckill' },
  { id: 3, name: 'Êñ∞È≤úÊ∞¥Êûú', tab: 'fruit' }
]
// tabÂàáÊç¢ÈÄªËæë
const useTagEffect = () => {
  const currentTab = ref(tabList[0].tab)
  const handleTabClick = tab => {
    currentTab.value = tab
  }
  return { currentTab, handleTabClick }
}
// Ëé∑ÂèñÂïÜÂìÅÂàóË°®
const useShopListEffect = (currentTab, shopId) => {
  const data = reactive({ goodsList: [] })
  // Ëé∑ÂèñÂïÜÂìÅÂàóË°®
  const getGoodsList = async () => {
    const result = await get(`/api/shop/${shopId}/products`, {
      tab: currentTab.value
    })
    console.log(
      'üöÄ ~ file: Content.vue ~ line 100 ~ getGoodsList ~ result',
      result
    )
    if (result.errno === 0 && result.data.length) {
      data.goodsList = result.data
    }
  }
  watchEffect(() => getGoodsList())
  const { goodsList } = toRefs(data)
  return { goodsList }
}
export default {
  name: 'shop-content',
  props: ['shopName'],
  setup() {
    // routeËé∑ÂèñÂïÜÂ∫óID
    const route = useRoute()
    const shopId = route.params.id
    const { currentTab, handleTabClick } = useTagEffect()
    const { goodsList } = useShopListEffect(currentTab, shopId)
    const { changeCartItemInfo, cartList } = useCartEffect()
    return {
      shopId,
      tabList,
      currentTab,
      handleTabClick,
      goodsList: goodsList,
      changeCartItemInfo,
      cartList
    }
  }
}
</script>

